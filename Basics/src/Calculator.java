class Calculator
{
    static int add(int input1, int input2)
    {
        return input1 + input2;
    }

    static int subtract(int input1, int input2)
    {
        return input1 - input2;
    }

    static int multiply(int input1, int input2)
    {
        return input1 * input2;
    }

    static int divide(int input1, int input2)
    {
        return input1 / input2;
    }
}
